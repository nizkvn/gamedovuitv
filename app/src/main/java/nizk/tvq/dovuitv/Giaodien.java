package nizk.tvq.dovuitv;

class Giaodien {
    int id, hinhanh;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(int hinhanh) {
        this.hinhanh = hinhanh;
    }

    public Giaodien(int id, int hinhanh) {
        this.id = id;
        this.hinhanh = hinhanh;
    }
}
