package nizk.tvq.dovuitv;

public class baihat {
    private int id;
    private int file;

    public baihat(int id, int file) {
        this.id = id;
        this.file = file;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFile() {
        return file;
    }

    public void setFile(int file) {
        this.file = file;
    }
}
